def is_valid_fund_code(code):
    """检查是否为有效的基金代码"""
    if code in EXCLUDE_CODES:
        return False
    if not code.startswith(VALID_PREFIXES):
        return False
    return True

def extract_fund_codes_from_file(filepath):
    """从单个文件中提取基金代码"""
    codes = set()
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
            # 提取6位数字
            matches = FUND_CODE_PATTERN.findall(content)
            for code in matches:
                if is_valid_fund_code(code):
                    codes.add(code)
    except Exception as e:
        print(f"Error reading {filepath}: {e}")
    return codes

def extract_fund_codes_with_context(filepath):
    """从文件中提取基金代码及其上下文"""
    fund_info = []
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
            lines = content.split('\n')
            for line_num, line in enumerate(lines, 1):
                matches = FUND_CODE_PATTERN.findall(line)
                for code in matches:
                    if is_valid_fund_code(code):
                        fund_info.append({
                            'code': code,
                            'context': line.strip()[:200],
                            'file': os.path.basename(filepath),
                            'line': line_num
                        })
    except Exception as e:
        print(f"Error reading {filepath}: {e}")
    return fund_info

def classify_fund(code, context=''):
    """根据代码和上下文分类基金"""
    # 先检查已知代码
    for category, data in FUND_CATEGORIES.items():
        if code in data['codes']:
            return category
    
    # 根据上下文关键词分类
    context_lower = context.lower()
    for category, data in FUND_CATEGORIES.items():
        for keyword in data['names']:
            if keyword in context:
                return category
    
    # 根据代码前缀初步判断
    if code.startswith('51') or code.startswith('56') or code.startswith('58'):
        return 'broad'  # 默认为宽基
    elif code.startswith('15'):
        return 'sector'  # 默认为行业
    elif code.startswith('16'):
        return 'overseas'  # 默认为QDII
    
    return 'unknown'

def get_market(code):
    """判断所属市场"""
    if code.startswith(('51', '56', '58', '50')):
        return '沪市'
    elif code.startswith(('15', '16', '18')):
        return '深市'
    return '未知'

def get_asset_type(category):
    """判断资产类别"""
    asset_map = {
        'broad': '股票',
        'sector': '股票',
        'commodity': '商品',
        'bond': '债券',
        'overseas': '股票'
    }
    return asset_map.get(category, '未知')

def main():
    print("=" * 60)
    print("E大基金代码提取工具")
    print("=" * 60)
    
    # 确保输出目录存在
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    
    # 获取所有文章文件
